# Программа о выборе витязя на распутье
print("Картина Васнецова «Витязь на распутье»")
print("Стоит витязь перед камнем с надписью:")
print("«Налево пойти — коня потерять, себя спасти;")
print("Направо пойти — себя потерять, коня спасти;")
print("Прямо пойти — женатым быть»")
print()

# Предлагаем пользователю выбрать путь за витязя
choice = input("Куда поедет витязь? (налево/направо/прямо): ").lower()

# Описываем последствия каждого выбора
if choice == "налево":
    print("\nВитязь повернул налево:")
    print("Выехал он в чисто поле, и тут налетели на него вороги.")
    print("Бился витязь храбро, коня под ним убили, но сам он выжил.")
    print("Итог: Коня потерял, себя спас — как на камне написано.")

elif choice == "направо":
    print("\nВитязь повернул направо:")
    print("Попал он в тёмный лес, где жили злые разбойники.")
    print("Отбился витязь от врагов, коня уберёг, да сам был ранен тяжко.")
    print("Итог: Себя потерял (чуть не погиб), коня спас — как на камне написано.")

elif choice == "прямо":
    print("\nВитязь поехал прямо:")
    print("Приехал он в славный город, встретил красну девицу.")
    print("Сыграли свадьбу, и жили они в любви и согласии долгие годы.")
    print("Итог: Женатым быть — как на камне написано.")

else:
    print("\nЗадумался витязь крепко...")
    print("Не знает, какую судьбу выбрать. Коня жалко, и себя жаль.")
    print("А камень старый, мохом порос, три пути указывает.")
    print("Итог: Стоит витязь в раздумье, выбирает долю.")