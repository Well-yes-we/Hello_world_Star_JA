# Программа для определения возможности переливания крови
# Принимаем группы крови донора и пациента
donor = input("Введите группу крови донора (A, B, AB или O): ").upper()
patient = input("Введите группу крови пациента (A, B, AB или O): ").upper()

# Проверяем возможность переливания
if donor == patient:
    print("Переливание возможно: группы крови совпадают")
elif donor == "O":
    print("Переливание возможно: донор с нулевой группой (универсальный донор)")
else:
    print("Переливание НЕВОЗМОЖНО: группы несовместимы")